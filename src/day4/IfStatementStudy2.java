package day4;

public class IfStatementStudy2 {
    public static void main(String[] args) {

        int a = 5;

        // % 연산자는 두 수의 나머지를 구하는 연산자입니다.
        if(a % 2 == 0){
            System.out.println(a + "는 짝수입니다.");
        }else{
            System.out.println(a + "는 홀수입니다.");
        }

    }
}
